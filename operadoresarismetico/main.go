package main

import "fmt"

func main() {

	// Operadores aritmeticos
	var a = 2 + 3*4
	fmt.Println(a)

	// Operadores de asignacion
	var b = 10 // asignacion
	b += 10    // b = b + 10
	fmt.Println(b)

	//decremento
	var c = 10
	c-- // c = c - 1
	fmt.Println(c)

	//incremento
	var d = 10
	d++ // d = d + 1
	fmt.Println(d)
}
